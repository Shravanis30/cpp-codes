// C++ program to illustrate If statement
  
#include <iostream>
using namespace std;
  
int main()
{
    int i = 10;
  
    if (i < 15) 
    {
        cout << "10 is less than 15 \n";
    }
  
    cout << "I am Not in if";
}
//1. Program starts.
//2. i is initialized to 10.
//3. if-condition is checked. 10 < 15, yields true.
  //3.a) "10 is less than 15" gets printed.
//4. "I am Not in if" is printed.